/* Define Length Modifier Macros */
#define SHORT 1
#define LONG 2

/* Define Flag Modifier Macros */
#define ZERO 8
#define NEG 16
#define HASH 4
#define SPACE 2
#define PLUS 1
#define PLUS_FLAG (flags & 1)
#define SPACE_FLAG ((flags >> 1) & 1)
#define HASH_FLAG ((flags >> 2) & 1)
#define ZERO_FLAG ((flags >> 3) & 1)
#define NEG_FLAG ((flags >> 4) & 1)


/**
 * struct formatter - A new struct type defining a converter struct.
 * @symbol: A letter representing a format type
 * @func: A function pointer to a function that prints
 *	a format type corresponding to symbol.
 *
 */
typedef struct format {
	char *letter;
	unsigned int (*func)(va_list, buffer_t *, unsigned char,
			int, int, unsigned char);
} format_spec;

/**
 * struct buffer_struct - A new type defining a buffer struct.
 * @buffer: A pointer to a character array.
 * @start: A pointer to the first character.
 * @length: Length of the string stored in buffer.
 */
typedef struct buffer_struct
{
	char *buffer;
	char *start;
	unsigned int length;
} buffer_type;

/**
 * struct flag_struct - A new type definig a flags struct.
 * @flag: A character that represents a flag.
 * @value: The integer value of the flag.
 */
typedef struct flag_s
{
	unsigned char flag;
	unsigned char value;
} flag_type;

